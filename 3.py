print("KALKULATOR: \n")
d = int(input("Masukkan Angka: "))
f = int(input("Masukkan Angka Lagi: "))
hasil1 = d + f
hasil2 = d * f
hasil3 = d ** f
hasil4 = d - f
hasil5 = d / f
print("%s + %s =" % (f, d), hasil1)
print("%s x %s =" % (f, d), hasil2)
print("%s ^ %s =" % (f, d), hasil3)
print("%s - %s =" % (f, d), hasil4)
print("%s / %s =" % (f, d), hasil5)
print("Anda Memasukan angka: %s dan %s" % (f, d))















